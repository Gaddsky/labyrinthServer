package com.github.gaddsky.labyrinthServer.domain;


/**
 * This domain represents the record maze in maze's list in database. Each maze stored in individual table in database.
 */
public class LabyrinthRecord {
    private int id;
    private String name;
    private int width;
    private int height;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
